//Popup code adapted from (“A Very Simple Popup Box - HTML, CSS, JavaScript”)

//Mexico
$(window).load(function () {
  $(".trigger_popup_Mexico").click(function () {
    $(".hover_bkgr_Mexico").show();
  });
  $(".hover_bkgr_Mexico").click(function () {
    $(".hover_bkgr_Mexico").hide();
  });
  $(".popupCloseButton").click(function () {
    $(".hover_bkgr_Mexico").hide();
  });
});

