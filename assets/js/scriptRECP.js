//Carajillo
$(window).load(function () {
  $(".trigger_popup_Carajillo").click(function () {
    $(".hover_bkgr_Carajillo").show();
  });
  $(".hover_bkgr_Carajillo").click(function () {
    $(".hover_bkgr_Carajillo").hide();
  });
  $(".popupCloseButton").click(function () {
    $(".hover_bkgr_Carajillo").hide();
  });
});

